			<h2>Iekšējās kārtības un drošības noteikumi</h2>
			<p>
				Šie noteikumi nosaka kārtību, kādā tiek izmantotas SkillZone.lv telpas, inventārs, mākslīgā
				ledus laukums un sniegtie pakalpojumi.
			</p>

			<h3>1. Vispārīgie noteikumi</h3>
			<ol>
				<li>
					Šie iekšējās kārtības un drošības noteikumi (turpmāk – Noteikumi) nosaka kārtību, kādā
					tiek izmantotas SkillZone.lv telpas, inventārs, mākslīgā ledus laukums un sniegtie
					pakalpojumi.
				</li>
				<li>
					Noteikumi ir saistoši visiem SkillZone.lv apmeklētājiem, tostarp sportistiem, treneriem,
					vecākiem, skatītājiem un citām trešajām personām.
				</li>
				<li>
					Apmeklējot SkillZone.lv vai piedaloties treniņos, klients apliecina, ka ir iepazinies ar
					Noteikumiem, tos saprot un apņemas ievērot.
				</li>
				<li>
					SkillZone.lv patur tiesības atteikt pakalpojuma sniegšanu personām, kas neievēro šos
					Noteikumus vai apdraud sevi, citus apmeklētājus vai inventāru.
				</li>
			</ol>

			<h3>2. Treniņu raksturs un riski</h3>
			<ol>
				<li>
					Hokeja treniņi un ar tiem saistītās aktivitātes ir sportiskas nodarbības ar paaugstinātu
					traumu risku.
				</li>
				<li>
					Klients apzinās, ka treniņu laikā pastāv kritienu, sadursmju, savainojumu, pārslodzes un
					citu veselības apdraudējumu risks.
				</li>
				<li>
					SkillZone.lv nenodrošina medicīnisko uzraudzību treniņu laikā, ja vien tas nav iepriekš
					rakstiski saskaņots.
				</li>
			</ol>

			<h3>3. Atbildība par veselību</h3>
			<ol>
				<li>
					Klients pats ir pilnībā atbildīgs par savu veselības stāvokli un fizisko sagatavotību.
				</li>
				<li>
					Pirms treniņu uzsākšanas klients pats izvērtē savu veselības stāvokli un nepieciešamības
					gadījumā konsultējas ar ārstu.
				</li>
				<li>
					SkillZone.lv neuzņemas atbildību par traumām, veselības traucējumiem vai nāvi, kas
					radušies treniņu laikā vai to rezultātā, izņemot gadījumus, ja kaitējums nodarīts
					SkillZone.lv tīšas rīcības dēļ.
				</li>
				<li>
					Klients uzņemas pilnu atbildību par sekām, kas radušās nepareizas vingrinājumu izpildes,
					instrukciju neievērošanas vai pārslogojuma dēļ.
				</li>
			</ol>

			<h3>4. Nepilngadīgie</h3>
			<ol>
				<li>
					Personas, kas jaunākas par 18 gadiem, drīkst piedalīties treniņos tikai ar vecāku vai
					likumiskā aizbildņa piekrišanu.
				</li>
				<li>
					Vecāki vai aizbildņi uzņemas pilnu atbildību par nepilngadīgā veselību, uzvedību un
					Noteikumu ievērošanu.
				</li>
				<li>
					SkillZone.lv nav atbildīgs par nepilngadīgo uzraudzību ārpus organizēta treniņa laika.
				</li>
			</ol>

			<h3>5. Apģērbs un aizsargaprīkojums</h3>
			<ol>
				<li>Uz mākslīgā ledus laukuma drīkst atrasties tikai ar maiņas apaviem vai zeķēm.</li>
				<li>Ielas apavi uz laukuma ir stingri aizliegti higiēnas un drošības apsvērumu dēļ.</li>
				<li>Treniņu laikā obligāti jālieto aizsargaprīkojums.</li>
			</ol>

			<p><strong>Bērniem līdz 18 gadu vecumam</strong> obligāti jālieto pilna hokeja forma:</p>
			<ul>
				<li>hokeja ķivere ar sejas aizsargu</li>
				<li>kakla aizsargs</li>
				<li>plecu sargi</li>
				<li>elkoņu sargi</li>
				<li>hokeja cimdi</li>
				<li>hokeja bikses</li>
				<li>ceļu un stilbu sargi</li>
			</ul>

			<p><strong>Pieaugušajiem</strong> treniņu laikā obligāti jāvalkā hokeja ķivere.</p>
			<ol start="6">
				<li>
					Klients pats ir atbildīgs par sava aizsargaprīkojuma tehnisko stāvokli un atbilstību.
				</li>
				<li>
					SkillZone.lv ir tiesības liegt piekļuvi treniņam personām, kuras neievēro šīs prasības.
				</li>
			</ol>

			<h3>6. Inventārs un trenažieri</h3>
			<ol>
				<li>Visi SkillZone.lv trenažieri un inventārs jālieto tikai paredzētajam mērķim.</li>
				<li>Aizliegts patvaļīgi pārvietot, izjaukt vai modificēt inventāru.</li>
				<li>Klientam nekavējoties jāziņo par jebkādiem bojājumiem vai defektiem.</li>
				<li>
					Par tīši vai rupjas neuzmanības rezultātā nodarītiem bojājumiem klients ir materiāli
					atbildīgs normatīvajos aktos noteiktajā kārtībā.
				</li>
			</ol>

			<h3>7. Video novērošana un datu aizsardzība</h3>
			<ol>
				<li>
					SkillZone.lv telpās tiek veikta video novērošana drošības, inventāra aizsardzības un
					strīdu risināšanas nolūkos.
				</li>
				<li>Video ieraksti var tikt izmantoti tikai normatīvajos aktos atļautajos gadījumos.</li>
				<li>Apmeklējot telpas, klients apliecina piekrišanu video novērošanai.</li>
			</ol>

			<h3>8. Personīgās mantas</h3>
			<ol>
				<li>Klients pats ir atbildīgs par savām personīgajām mantām.</li>
				<li>
					SkillZone.lv neuzņemas atbildību par pazaudētām, bojātām vai nozagtām personīgajām mantām.
				</li>
			</ol>

			<h3>9. Uzvedība un kārtība</h3>
			<ol>
				<li>Aizliegta agresīva, bīstama vai citiem traucējoša uzvedība.</li>
				<li>
					Aizliegts atrasties treniņos alkohola, narkotisko vai citu apreibinošu vielu ietekmē.
				</li>
				<li>
					SkillZone.lv patur tiesības izraidīt personu bez kompensācijas, ja tiek pārkāpti
					Noteikumi.
				</li>
			</ol>

			<h3>10. Ugunsdrošība un ārkārtas situācijas</h3>
			<ol>
				<li>Klientiem jāievēro ugunsdrošības noteikumi un personāla norādījumi.</li>
				<li>Ārkārtas situācijā nekavējoties jāatstāj telpas, ievērojot evakuācijas norādes.</li>
			</ol>

			<h3>11. Noslēguma noteikumi</h3>
			<ol>
				<li>
					SkillZone.lv patur tiesības jebkurā laikā grozīt Noteikumus bez iepriekšēja brīdinājuma.
				</li>
				<li>Aktuālā Noteikumu versija ir publicēta SkillZone.lv mājaslapā.</li>
				<li>Strīdi tiek risināti saskaņā ar Latvijas Republikas normatīvajiem aktiem.</li>
			</ol>
